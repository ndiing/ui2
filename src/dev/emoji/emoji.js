const data = [];

for (const emoji of emojis) {
    const label = emoji.name;
    const emojis = emoji.glyphs.split(' ');
    data.push(label);
    let cols = 12;
    let rows = Math.ceil(emojis.length / cols);
    for (let i = 0; i < rows; i++) {
        let start = i * cols;
        let end = start + cols;
        data.push(emojis.slice(start, end));
    }
}

